package frc.robot.commands.test;

import edu.wpi.first.wpilibj.smartdashboard.SmartDashboard;
import edu.wpi.first.wpilibj2.command.Command;
import frc.robot.subsystems.Drive;

public class PrintGyro extends Command {

    private final Drive drive;
  
    /** Creates a new PrintGyro. */
    public PrintGyro(Drive drive) {
        addRequirements(this.drive = drive);
    }

    // Called when the command is initially scheduled.
    @Override
    public void initialize() {
    }

    // Called every time the scheduler runs while the command is scheduled.
    @Override
    public void execute() {
        SmartDashboard.putNumberArray("gyro x, y, z", new double[] {drive.getGyroX(), drive.getGyroY(), drive.getGyroZ()});
    }

    // Called once the command ends or is interrupted.
    @Override
    public void end(boolean interrupted) {
    }

    // Returns true when the command should end.
    @Override
    public boolean isFinished() {
        return false;
    }
}
